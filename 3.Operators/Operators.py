# 1.Arithmetic Operators:
# Modulus (Remainder): %
# example
print(17%5)          #2
print(19%5)          #4
print(17%3)          #2
print(15%3)          #0

# Exponentiation: **
# example
print(2**4)          #16
print(5**4)          #625     #5*5*5*5

# Floor Division: //
# example
print(19/5)          #3.8
print(19//5)          #3          #remove after points

# 2.Comparison (Relational) Operators:
print(4==5)          #False
print(4!=5)          #True

# 4.Logical Operators:
print(4==5 and 4!=5)     #False
print(4==4 and 4!=5)     #True
print(4==5 or 4!=5)     #True          #or look both if one is true then it says true
print(not 4==5)     #True

# 3.Assignment Operators:
a=5
a+=20
print(a)     #25

